export default {
  data() {
    return {
      login: {
        token: 'asdf',
        password: 'password',
      },
      showPassword: false,
    }
  },
  methods: {
    async userLogin() {
      try {
        let response = await this.$auth.loginWith('local', { data: this.login })
        console.log(response)
      } catch (err) {
        this.$toast.error(err.response.status +" | " +err.response.data.error)
        console.log(err.response)
      }
    },
  },
}
</script>
